package com.skillstorm;

public class Pillars {

	public static void main(String[] args) {
		
		// THE FOUR PILLARS OF OBJECT-ORIENTED PROGRAMMING
		
		/**
		 * Why use OOP?
		 * - model data used in the real world
		 * - building systems to help solve problems
		 * - easy to read and add to code when wrapped into objects
		 * - reusable
		 * - think about/manage state
		 * - separating/maintaining things in separate classes
		 * 
		 * What are the four pillars?
		 * 
		 * Encapsulation
		 * - wrapping properties/behaviors into a class
		 * - controls access to properties/behaviors
		 * 
		 * Abstraction
		 * - hide the details of the implentation from the user
		 * - set up abstract "containers" for things that indicate their functionality without providing implentation
		 * - abstract classes, interfaces, various design patterns
		 * 
		 * Inheritance
		 * - parent/child classes
		 * - child takes properties/methods from the parent
		 * - can add/override things to customize to the child
		 * - code reusability
		 * 
		 * Polymorphism
		 * - "many shapes" "different forms"
		 * - can group Objects by parent class, collect them together, a child IS-A parent
		 * - method overriding, runtime polymorphism -- can call the parent method, and it'll select the child's functionality
		 * - method overloading, compile-time polymorphism (same name, different parameters)
		 * 
		 */

	}

}
