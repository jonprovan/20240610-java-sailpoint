package com.skillstorm.models;

// a sample object to fill with an appropriate message and return it
public class Message {
	
	private String msg;
	
	public Message() {
		super();
	}
	
	public Message(String msg) {
		this.msg = msg;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

}
